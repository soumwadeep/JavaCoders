//Program to add two numbers using Java.
class Sumation
{
public static void main(String args[])
{
int a,b,sum;  // Declearing int type variable.
a=10;         // assigning value.
b=7;          // assigning value.
sum=a+b;      // dynamic allocation.
System.out.println("Result = "+sum); // print the result
}
}

